#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";

const OUTPUT_PATH = path.resolve("Sources/App/GeneratedHealthKitCatalog.swift");
const IOS_DEPLOYMENT_TARGET = "18.0";

const DATASETS = [
  {
    swiftType: "HKQuantityTypeIdentifier",
    constantName: "quantityEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkquantitytypeidentifier.json",
      "https://developer.apple.com/tutorials/data/documentation/healthkit/nutrition-type-identifiers.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKQuantityTypeIdentifier/",
  },
  {
    swiftType: "HKCategoryTypeIdentifier",
    constantName: "categoryEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkcategorytypeidentifier.json",
      "https://developer.apple.com/tutorials/data/documentation/healthkit/symptom-type-identifiers.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKCategoryTypeIdentifier/",
  },
  {
    swiftType: "HKCharacteristicTypeIdentifier",
    constantName: "characteristicEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkcharacteristictypeidentifier.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKCharacteristicTypeIdentifier/",
  },
  {
    swiftType: "HKCorrelationTypeIdentifier",
    constantName: "correlationEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkcorrelationtypeidentifier.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKCorrelationTypeIdentifier/",
  },
  {
    swiftType: "HKClinicalTypeIdentifier",
    constantName: "clinicalEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkclinicaltypeidentifier.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKClinicalTypeIdentifier/",
  },
  {
    swiftType: "HKDocumentTypeIdentifier",
    constantName: "documentEntries",
    urls: [
      "https://developer.apple.com/tutorials/data/documentation/healthkit/hkdocumenttypeidentifier.json",
    ],
    prefix: "doc://com.apple.healthkit/documentation/HealthKit/HKDocumentTypeIdentifier/",
  },
];

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

async function main() {
  const generatedAt = new Date().toISOString();
  const sections = [];

  for (const dataset of DATASETS) {
    const entries = await collectEntries(dataset);
    sections.push(renderDataset(dataset, entries));
  }

  const output = [
    "import Foundation",
    "import HealthKit",
    "",
    "// Generated by scripts/generate-healthkit-registry.mjs.",
    `// Source refresh timestamp: ${generatedAt}`,
    "enum GeneratedHealthKitCatalog {",
    `    static let generatedAt = ${JSON.stringify(generatedAt)}`,
    "",
    ...sections.flatMap((section) => section),
    "}",
    "",
  ].join("\n");

  await fs.writeFile(OUTPUT_PATH, output, "utf8");
}

async function collectEntries(dataset) {
  const seen = new Set();
  const entries = [];

  for (const url of dataset.urls) {
    const document = await fetchJSON(url);
    const references = document.references ?? {};

    for (const section of document.topicSections ?? []) {
      for (const identifier of section.identifiers ?? []) {
        if (!identifier.startsWith(dataset.prefix)) {
          continue;
        }

        const symbol = identifier.slice(dataset.prefix.length);
        if (!isSwiftMember(symbol) || seen.has(symbol)) {
          continue;
        }

        seen.add(symbol);
        const title = references[identifier]?.title ?? symbol;
        const symbolURL = references[identifier]?.url;
        entries.push({
          symbol,
          displayName: formatDisplayName(title),
          introducedAt: symbolURL ? await fetchIOSAvailability(symbolURL) : "0.0",
        });
      }
    }
  }

  return entries;
}

async function fetchJSON(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.json();
}

async function fetchIOSAvailability(symbolPath) {
  const document = await fetchJSON(`https://developer.apple.com/tutorials/data${symbolPath}.json`);
  const iosPlatform = (document.metadata?.platforms ?? []).find((platform) => platform.name === "iOS");
  return iosPlatform?.introducedAt ?? "0.0";
}

function isSwiftMember(symbol) {
  return /^[A-Za-z_][A-Za-z0-9_]*$/u.test(symbol);
}

function formatDisplayName(symbol) {
  const overrides = new Map([
    ["vo2Max", "VO2 Max"],
    ["appleExerciseTime", "Apple Exercise Time"],
    ["appleMoveTime", "Apple Move Time"],
    ["appleStandTime", "Apple Stand Time"],
    ["appleSleepingWristTemperature", "Apple Sleeping Wrist Temperature"],
    ["appleWalkingSteadiness", "Apple Walking Steadiness"],
    ["CDA", "CDA"],
    ["nikeFuel", "Nike Fuel"],
    ["uvExposure", "UV Exposure"],
  ]);

  if (overrides.has(symbol)) {
    return overrides.get(symbol);
  }

  return symbol
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1 $2")
    .replace(/\bVo2\b/g, "VO2")
    .replace(/\bUv\b/g, "UV")
    .replace(/\bSdnn\b/g, "SDNN")
    .replace(/\bCda\b/g, "CDA")
    .replace(/\bHk\b/g, "HK")
    .replace(/\bId\b/g, "ID")
    .trim()
    .replace(/\s+/g, " ")
    .split(" ")
    .map((word) => {
      if (word === word.toUpperCase()) {
        return word;
      }

      return word.charAt(0).toUpperCase() + word.slice(1);
    })
    .join(" ");
}

function renderDataset(dataset, entries) {
  const baselineEntries = entries.filter((entry) => compareVersions(entry.introducedAt, IOS_DEPLOYMENT_TARGET) <= 0);
  const gatedEntries = entries.filter((entry) => compareVersions(entry.introducedAt, IOS_DEPLOYMENT_TARGET) > 0);
  const gatedByVersion = new Map();

  for (const entry of gatedEntries) {
    const list = gatedByVersion.get(entry.introducedAt) ?? [];
    list.push(entry);
    gatedByVersion.set(entry.introducedAt, list);
  }

  return [
    `    static var ${dataset.constantName}: [GeneratedHealthKitCatalogEntry<${dataset.swiftType}>] {`,
    `        var output: [GeneratedHealthKitCatalogEntry<${dataset.swiftType}>] = [`,
    ...baselineEntries.map((entry) =>
      `        .init(symbol: ${JSON.stringify(entry.symbol)}, displayName: ${JSON.stringify(entry.displayName)}, identifier: .${entry.symbol}),`,
    ),
    "        ]",
    "",
    ...Array.from(gatedByVersion.entries())
      .sort((a, b) => compareVersions(a[0], b[0]))
      .flatMap(([version, versionEntries]) => [
        `        if #available(iOS ${version}, *) {`,
        ...versionEntries.map((entry) =>
          `            output.append(.init(symbol: ${JSON.stringify(entry.symbol)}, displayName: ${JSON.stringify(entry.displayName)}, identifier: .${entry.symbol}))`,
        ),
        "        }",
        "",
      ]),
    "        return output",
    "    }",
    "",
  ];
}

function compareVersions(left, right) {
  const leftParts = String(left).split(".").map((part) => Number.parseInt(part, 10) || 0);
  const rightParts = String(right).split(".").map((part) => Number.parseInt(part, 10) || 0);
  const partCount = Math.max(leftParts.length, rightParts.length);

  for (let index = 0; index < partCount; index += 1) {
    const difference = (leftParts[index] ?? 0) - (rightParts[index] ?? 0);
    if (difference !== 0) {
      return difference;
    }
  }

  return 0;
}
